// Create a flat list of all locations (provinces + cities)
export const getAllThailandLocations = (): string[] => {
    const allLocations: string[] = [];

    THAILAND_LOCATIONS.forEach(location => {
        // Add province name
        allLocations.push(location.province);

        // Add all cities (filter out duplicates with province names)
        location.cities.forEach(city => {
            if (!allLocations.includes(city)) {
                allLocations.push(city);
            }
        });
    });

    return allLocations.sort();
};

// Get provinces only
export const getThailandProvinces = (): string[] => {
    return THAILAND_LOCATIONS.map(location => location.province).sort((a, b) => a.localeCompare(b));
};

// Get cities by province
export const getCitiesByProvince = (province: string): string[] => {
    const location = THAILAND_LOCATIONS.find(loc => loc.province === province);
    return location ? location.cities.sort() : [];
};

// Search locations by keyword
export const searchThailandLocations = (keyword: string): string[] => {
    const allLocations = getAllThailandLocations();
    return allLocations.filter(location =>
        location.toLowerCase().includes(keyword.toLowerCase())
    );
};